template<typename T> T gcd(T a, T b) { return b ? gcd(b, a % b) : a; }
template<typename T> T lcm(T a, T b) { return a / gcd(a, b) * b; }

int solution(int N, int M) {
  ll l = lcm((ll)N, (ll)M);
  return l / M;
}
